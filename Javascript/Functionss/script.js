// 10. Convert Celsius to Fahrenheit.

function celsiusToFahrenheit(celsius) {
    console.log((celsius * 9/5) + 32);   // Celsius to Fahrenheit formula      // 25 × 9 = 225
}                                                                             // 225 ÷ 5 = 45
celsiusToFahrenheit(25); // Output: 77                                        // 45 + 32 = 77

